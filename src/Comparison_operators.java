public class Comparison_operators {
    public static void main(String[] args) {
        //a==b
        int a=3;
        int b=4;
        System.out.println(a==b);//false as the values are not same
        System.out.println(a!=b);//opposite of false will be printed
        System.out.println(a<b);//true as 3 is smaller than 4
        System.out.println(a>b);//false as 3 is not bigger than 4
        System.out.println(a>=b);//false as 3 is not bigger than or equal to 4
        System.out.println(a<=b);//true as 3 is smaller than 4
    }
}
